export function getWhatItIsIssues(whatItIs) {
  return validateEditorialCopy({
    what_it_is: whatItIs,
    the_mood: "Direct, plain, and concise.",
  }).issues.filter((issue) => issue.startsWith("what_it_is"));
}

export function validateWhatItIsOnly(whatItIs) {
  const issues = getWhatItIsIssues(whatItIs);

  return {
    ok: issues.length === 0,
    issues,
    what_it_is: cleanupEditorialField(whatItIs),
  };
}

export function validateMoodOnly(theMood, whatItIs = "") {
  const mood = cleanupEditorialField(theMood);
  const issues = [];

  if (!mood) {
    issues.push("the_mood is empty");
  }

  if (countWords(mood) > EDITORIAL_COPY_LIMITS.the_mood) {
    issues.push(`the_mood exceeds ${EDITORIAL_COPY_LIMITS.the_mood} words`);
  }

  for (const phrase of findBannedPhrases(mood)) {
    issues.push(`the_mood contains interchangeable phrasing: ${phrase}`);
  }

  for (const phrase of BANNED_MOOD_PHRASES.filter((pattern) => pattern.test(mood)).map(String)) {
    issues.push(`the_mood uses generic viewer-note filler: ${phrase}`);
  }

  if (mood.split(/[.!?]+/).filter(Boolean).length > 1) {
    issues.push("the_mood must be one compact sentence or fragment");
  }

  issues.push(...findMoodRestraintIssues(mood));

  if (whatItIs) {
    const sharedLongWords = whatItIs
      .toLowerCase()
      .split(/\W+/)
      .filter((word) => word.length >= 7)
      .filter((word) => mood.toLowerCase().includes(word));

    if (sharedLongWords.length >= 2) {
      issues.push("what_it_is and the_mood repeat the same descriptive language");
    }
  }

  return {
    ok: issues.length === 0,
    issues,
    the_mood: mood,
  };
}
