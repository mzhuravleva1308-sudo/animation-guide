export function countWords(text) {
  return text.trim().split(/\s+/).filter(Boolean).length;
}

export function cleanupEditorialField(text) {
  return String(text ?? "")
    .trim()
    .replace(/^["']+|["']+$/g, "")
    .replace(/\s+/g, " ")
    .replace(/\s+([,.!?;:])/g, "$1");
}

export function findBannedPhrases(text) {
  return BANNED_PHRASES.filter((pattern) => pattern.test(text)).map(String);
}

export function findBannedWhatItIsPhrases(text) {
  return BANNED_WHAT_IT_IS_PHRASES.filter((pattern) => pattern.test(text)).map(
    String
  );
}

export function findWeakPremiseVerbs(text) {
  return WEAK_PREMISE_VERBS.filter((pattern) => pattern.test(text)).map(String);
}

export function findAbstractThemeWords(text) {
  const lower = text.toLowerCase();
  return ABSTRACT_THEME_WORDS.filter((word) => lower.includes(word));
}

export function countMoodAbstractNouns(text) {
  const lower = text.toLowerCase();
  return MOOD_ABSTRACT_NOUNS.filter((word) =>
    new RegExp(`\\b${word}\\b`, "i").test(lower)
  );
}

export function findMoodRestraintIssues(text) {
  const issues = [];

  if (/\blike\b/i.test(text)) {
    issues.push('the_mood must not use similes or the word "like"');
  }

  if (/\bas if\b/i.test(text) || /\bas though\b/i.test(text)) {
    issues.push("the_mood must not use simile constructions such as as if or as though");
  }

  if (/\breveals?\b/i.test(text)) {
    issues.push('the_mood must not use interpretive verbs such as "reveals"');
  }

  if (/\blingers?\b/i.test(text)) {
    issues.push('the_mood must not use decorative verbs such as "lingers" or "lingering"');
  }

  const abstractNouns = countMoodAbstractNouns(text);
  if (abstractNouns.length > 1) {
    issues.push(
      `the_mood uses too many abstract nouns (${abstractNouns.join(", ")})`
    );
  }

  return issues;
}

export function validateEditorialCopy(copy) {
  const whatItIs = cleanupEditorialField(copy.what_it_is);
  const theMood = cleanupEditorialField(copy.the_mood);
  const issues = [];

  if (!whatItIs) {
    issues.push("what_it_is is empty");
  }

  if (!theMood) {
    issues.push("the_mood is empty");
  }

  if (countWords(whatItIs) > EDITORIAL_COPY_LIMITS.what_it_is) {
    issues.push(`what_it_is exceeds ${EDITORIAL_COPY_LIMITS.what_it_is} words`);
  }

  if (countWords(theMood) > EDITORIAL_COPY_LIMITS.the_mood) {
    issues.push(`the_mood exceeds ${EDITORIAL_COPY_LIMITS.the_mood} words`);
  }

  for (const phrase of findBannedPhrases(whatItIs)) {
    issues.push(`what_it_is contains interchangeable phrasing: ${phrase}`);
  }

  for (const phrase of findBannedWhatItIsPhrases(whatItIs)) {
    issues.push(`what_it_is uses dramatic trailer phrasing: ${phrase}`);
  }

  for (const phrase of BANNED_MOOD_PHRASES.filter((pattern) => pattern.test(theMood)).map(String)) {
    issues.push(`the_mood uses generic viewer-note filler: ${phrase}`);
  }

  for (const phrase of findBannedPhrases(theMood)) {
    issues.push(`the_mood contains interchangeable phrasing: ${phrase}`);
  }

  for (const verb of findWeakPremiseVerbs(whatItIs)) {
    issues.push(`what_it_is uses weak catalog verb pattern: ${verb}`);
  }

  for (const word of findAbstractThemeWords(whatItIs)) {
    issues.push(
      `what_it_is uses abstract or psychological wording "${word}" instead of visible story material`
    );
  }

  if (
    /\ba new life\b/i.test(whatItIs) &&
    !/\b(Denmark|Paris|Tokyo|London|America|Europe|Afghanistan)\b/i.test(whatItIs)
  ) {
    issues.push('what_it_is uses vague "a new life" without a specific place');
  }

  if (whatItIs.split(/[.!?]+/).filter(Boolean).length > 2) {
    issues.push("what_it_is has more than two sentence fragments");
  }

  if (theMood.split(/[.!?]+/).filter(Boolean).length > 1) {
    issues.push("the_mood must be one compact sentence or fragment");
  }

  issues.push(...findMoodRestraintIssues(theMood));

  const sharedLongWords = whatItIs
    .toLowerCase()
    .split(/\W+/)
    .filter((word) => word.length >= 7)
    .filter((word) => theMood.toLowerCase().includes(word));

  if (sharedLongWords.length >= 2) {
    issues.push("what_it_is and the_mood repeat the same descriptive language");
  }

  return {
    ok: issues.length === 0,
    issues,
    copy: {
      what_it_is: whatItIs,
      the_mood: theMood,
    },
  };
}

export function buildEditorialCopyPrompt(film, repairNote = "") {
  const repairSection = repairNote
    ? `\nRepair note from the previous attempt:\n${repairNote}\n`
    : "";

  return `
You write restrained film notes for an animation guide — factual, specific, and plain-spoken.

Write as if you watched this film recently and are leaving a short note for a friend. Not a trailer. Not a poem. Not a theme essay.

Film:
Title: ${film.title}
Original title: ${film.original_title ?? ""}
Director: ${film.director ?? ""}
Year: ${film.year ?? ""}
Country: ${film.country ?? ""}
Duration: ${film.duration_minutes ?? ""} min
Technique: ${film.technique ?? ""}
Moods: ${(film.moods ?? []).join(", ")}
Aesthetic tags: ${(film.aesthetic_tags ?? []).join(", ")}
Narrative tags: ${(film.narrative_tags ?? []).join(", ")}
Synopsis: ${film.synopsis ?? ""}

Before writing each field, apply this test:
Could this exact sentence plausibly fit at least 20 other animated films?
If yes, rewrite it with details that belong specifically to THIS film.

Field 1 — "what_it_is":
- One sentence, max two short clauses.
- Neutral, factual wording about visible story material.
- Use concrete characters, setting, conflict, rules, jobs, objects, or film form.
- Include at least two concrete anchors from this film.
- Prefer plain verbs: tells, follows, hides, steals, escapes, joins, hunts, builds, loses, reunites, is thrown into, is killed in.
- For interview/documentary animation, name the speaker, the country he fled, the country he lives in now, and what he is finally saying out loud.
  Example: "An Afghan man tells, for the first time, how he fled his country as a child and built a new life in Denmark while hiding parts of his past."
- Do not interpret psychology, symbolism, or hidden meaning.
- Do not claim internal motivations unless they are explicit on screen.

Too dramatic:
"After a fatal encounter in a sleazy diner, a timid manga artist escapes into a kaleidoscopic world of gangsters, surreal monsters, and his own psyche while chasing lost love."

Better:
"After being killed in a diner, a shy manga artist is thrown into a wildly changing world of gangsters, strange creatures and impossible escapes."

Avoid in what_it_is:
"daring rescue", "sinister schemes", "plunges the world into chaos", "chasing lost love", "his own psyche", "revealing hidden truths", "unexpected bond", "magical enchantment"

Field 2 — "the_mood":
- One compact sentence or fragment.
- Describe the viewing experience directly: pace, emotional pressure, humor, visual texture, warmth/coldness, noise/silence, darkness/lightness, realism/stylization.
- No plot recap. No audience advice. No interpretation.
- Do not use similes, metaphors, or the word "like".
- Do not use "as if" or "as though".

Good mood examples:
Mind Game: "Hyperactive, vulgar, euphoric and disorienting; it keeps changing shape before you can settle into it."
Flee: "Quiet and fragile, with an emotional tension that grows through what is left unsaid."
Paprika: "Fast, glossy and increasingly unnerving as dreams start leaking into ordinary life."
Song of the Sea: "Gentle and mournful, with a quiet bedtime-story rhythm and bursts of folklore magic."
The Boxtrolls: "Tactile, funny and slightly grotesque, with more nervous energy than its cozy look suggests."

Never use in the_mood:
"like", "as if", "fever dream", "lullaby", "kaleidoscope", "brushstroke", "underbelly", "swells", "lingers", "reveals", "sensory overload", "a blend of", "impending dread"

The two fields must do different jobs and must not repeat each other.

Limits:
- what_it_is: max ${EDITORIAL_COPY_LIMITS.what_it_is} words
- the_mood: max ${EDITORIAL_COPY_LIMITS.the_mood} words
- Return only JSON, no markdown
${repairSection}
Format:
{
  "what_it_is": "...",
  "the_mood": "..."
}
`.trim();
}

